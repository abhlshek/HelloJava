public class logicalopjava {
    public static void main(String[] args) {
        boolean isAdult=true;
        boolean hasLicense=false;
        
        //and operator
        
        System.out.println(isAdult && hasLicense);
        //or operator
        System.out.println(isAdult || hasLicense);
        // Not operator
        System.out.println(! isAdult);
                
                
    }
    
}
